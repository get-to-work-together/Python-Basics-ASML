name = 'Guido'
age = 62

# Guido is 62 years old.

print(name + ' is ' + str(age) + ' years old.')    # concatenation

print(name, 'is', age, 'years old.')   # print function

print(f'{name} is {age} years old.')   # f string

print(f'%s is %d years old.' % (name, age))   # format operator %

print('{} is {} years old.'.format(name, age))   # format method
